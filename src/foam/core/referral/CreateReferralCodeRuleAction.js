foam.CLASS({
  package: 'foam.core.referral',
  name: 'CreateReferralCodeRuleAction',

  documentation: `Create a referral code on user creation.`,

  implements: ['foam.core.ruler.RuleAction'],

  javaImports: [
    'foam.lang.ContextAgent',
    'foam.lang.X',
    'foam.dao.DAO',
    'foam.core.app.AppConfig',
    'foam.core.auth.User'
  ],

  methods: [
    {
      name: 'applyAction',
      javaCode: `
      agency.submit(x, new ContextAgent() {
        @Override
        public void execute(X x) {
          User user = (User) obj;
          AppConfig config = (AppConfig) x.get("appConfig");
          DAO referralCodeDAO = (DAO) ruler.getX().get("referralCodeDAO");
          ReferralCode referralCode = new ReferralCode();
          referralCode.setMenu("sign-up");
          referralCode.setWebsite(config.getUrl());
          referralCode.setQuery("referral");
          referralCode.setReferrer(user.getId());
          referralCode.setSpid(user.getSpid());
          referralCode.setAutoGenerated(true);
          referralCode.setEnabled(true);
          referralCodeDAO.put_(ruler.getX(), referralCode);
        }
      }, "Creating a referral code");
      `
    }
  ]
});
